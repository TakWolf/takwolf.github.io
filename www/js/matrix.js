const fontColors = [
    '#3aff33',
    '#404aff',
    '#ff0000',
    '#f2ff40',
    '#ff07d6',
    '#ffffff',
]
const fontSize = 24
const yOffset = 20

let currentFontColor = 0
let changeFontColorCountdown = 200

const positions = []

function random_init_y() {
    return Math.floor(Math.random() * -100) - 1
}

function resize() {
    canvas.width = window.innerWidth
    canvas.height = window.innerHeight

    const columns = Math.ceil(canvas.width / fontSize)
    while (positions.length > columns) {
        positions.pop()
    }
    while (positions.length < columns) {
        positions.push(random_init_y())
    }
}

function render() {
    ctx.fillStyle = 'rgba(0, 0, 0, 0.05)'
    ctx.fillRect(0, 0, canvas.width, canvas.height)

    changeFontColorCountdown -= 1
    if (changeFontColorCountdown <= 0) {
        changeFontColorCountdown = Math.floor(Math.random() * 400)
        currentFontColor = Math.floor(Math.random() * fontColors.length)
    }

    ctx.fillStyle = fontColors[currentFontColor]
    ctx.font = `${fontSize}px ark-pixel-12-monospaced-zh_cn, monospace`

    for (let x = 0; x < positions.length; x++) {
        let y = positions[x]

        if (y >= 0) {
            const text = chars.charAt(Math.floor(Math.random() * chars.length))
            ctx.fillText(text, x * fontSize, y * fontSize + yOffset)
        }

        y += 1
        if (y * fontSize >= canvas.height) {
            y = random_init_y()
        }

        positions[x] = y
    }
}

window.addEventListener('resize', resize)
resize()
setInterval(render, 36)
